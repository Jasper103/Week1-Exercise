package week1exercise.part1.KeyboardInput;

public class AgeInFiveYears {
    public static void main(String[] args) {
        String name = "Jasper";
        int age = 19;
        int Age = age + 5;
        int AGE = age - 5;
        System.out.println("Hello. What is your name? "
        + name + "\n" + "\nHi, " + name + "! How old are you? "
        + age + "\n" + "\nDid you know that in five years you will be "
                + Age + " years old?" + "\nAnd five years ago you were "
        + AGE + "! Imagine that!");
    }
}
